    private void muatKategori() {
        String sql = "select * from koleksi";
        koneksiDb kDb = new koneksiDb();
        ResultSet rs = koneksiDb.ambilData(sql);
        try {
            while (rs.next()) {
                kategori.addItem(rs.getInt("id") + "-" + rs.getString("namaKategori"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Buku.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void muatTabel() {
        DefaultTableModel model = (DefaultTableModel) tabel.getModel();
        model.getDataVector().removeAllElements();
        String sql = "select * from buku";
        koneksiDb kDb = new koneksiDb();
        ResultSet rs = koneksiDb.ambilData(sql);

        try {
            while (rs.next()) {
                Vector baris = new Vector();
                baris.add(rs.getInt("id"));
                baris.add(rs.getString("judul"));
                baris.add(rs.getString("penulis"));
                baris.add(rs.getString("penerbit"));
                baris.add(rs.getInt("tahunTerbit"));
                baris.add(rs.getString("stok"));
                baris.add(rs.getInt("kategori_id"));

                model.addRow(baris);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Kategori.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

