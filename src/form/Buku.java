    private void simpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_simpanMouseClicked
        // TODO add your handling code here:
        if (judul.getText().equals("") || penulis.getText().equals("") || penerbit.getText().equals("") || stok.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Input tidak boleh kosong");
        } else {
            String[] ktg = kategori.getSelectedItem().toString().split("-", 2);
            String sql = "insert into buku(judul,penulis,penerbit,tahunTerbit,stok,kategori_id) value('" + judul.getText() + "','" + penulis.getText() + "','" + penerbit.getText() + "','" + tahun_terbit.getYear() + "','" + stok.getText() + "'," + ktg[0] + ")";
            koneksiDb.ubahData(sql);
            muatTabel();
            kosongkan();
        }
    }//GEN-LAST:event_simpanMouseClicked

    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        // TODO add your handling code here:
        int baris = tabel.getSelectedRow();

        judul.setText(tabel.getValueAt(baris, 1).toString());
        penulis.setText(tabel.getValueAt(baris, 2).toString());
        penerbit.setText(tabel.getValueAt(baris, 3).toString());
        tahun_terbit.setYear((int) tabel.getValueAt(baris, 4));
        stok.setText(tabel.getValueAt(baris, 5).toString());

        String sql = "select * from koleksi where id = " + tabel.getValueAt(baris, 6);
        ResultSet rs = koneksiDb.ambilData(sql);
        try {
            while (rs.next()) {
                kategori.setSelectedItem(rs.getInt("id") + "-" + rs.getString("namaKategori"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Buku.class.getName()).log(Level.SEVERE, null, ex);
        }

        simpan.setEnabled(false);
        ubah.setEnabled(true);
        hapus.setEnabled(true);
    }//GEN-LAST:event_tabelMouseClicked

    private void ubahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ubahMouseClicked
        // TODO add your handling code here:
        String[] ktg = kategori.getSelectedItem().toString().split("-", 2);
        int row = tabel.getSelectedRow();
        String sql = "update buku set judul = '" + judul.getText() + "', penulis = '" + penulis.getText() + "', penerbit = '" + penerbit.getText() + "', tahunTerbit = " + tahun_terbit.getYear() + ", stok = " + stok.getText() + ", kategori_id= " + ktg[0] + " where id = " + tabel.getValueAt(row, 0);

        if (koneksiDb.ubahData(sql)) {
            JOptionPane.showMessageDialog(null, "Data buku berhasil diupdate");
            muatTabel();
            kosongkan();
        } else {
            JOptionPane.showMessageDialog(null, "Data buku gagal diupdate");
            kosongkan();
        }
    }//GEN-LAST:event_ubahMouseClicked

    private void hapusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_hapusMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tabel.getModel();
        int row = tabel.getSelectedRow();
        String sql = "delete from buku where id = " + tabel.getValueAt(row, 0);

        if (koneksiDb.ubahData(sql)) {
            JOptionPane.showMessageDialog(null, "Data buku berhasil dihapus");
            model.removeRow(row);
            kosongkan();
        } else {
            JOptionPane.showMessageDialog(null, "Data buku gagal dihapus");
            kosongkan();
        }
    }//GEN-LAST:event_hapusMouseClicked

